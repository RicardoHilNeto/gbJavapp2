    public void loading() {
        if (!beaches.isEmpty()) {
            namebeache1_txt.setText(beaches.get(0).getName());
            contaminantesbeache1_txt.setText(beaches.get(0).getContaminants());
            phbeache1_txt.setText(String.valueOf(beaches.get(0).pH));
            
            namebeache2_txt.setText(beaches.get(1).getName());
            contaminantesbeache2_txt.setText(beaches.get(1).getContaminants());
            phbeache2_txt.setText(String.valueOf(beaches.get(1).pH));
            
            namebeache3_txt.setText(beaches.get(2).getName());
            contaminantesbeache3_txt.setText(beaches.get(2).getContaminants());
            phbeache3_txt.setText(String.valueOf(beaches.get(2).pH));
        } else {
            namebeache1_txt.setText("No data available");
        }
    }

